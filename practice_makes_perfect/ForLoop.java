package practice_makes_perfect;

public class ForLoop {
    public static void main(String[] args) {
       //TODO FOR LOOP
        for(int i=1;i<=10;i++){
            System.out.println("this is for the value upto " +i);}
        for(int i=1;i<=5;i++){
            System.out.println("Star values" +i);}
        //TODO WHILE LOOP
        int n=1;
        while(n<=5){
            System.out.println("print me " +n);
            n++;
        }
        //TODO DO WHILE LOOP
        int m=0;
        do{
            System.out.println("welcome to the screen" +m);
        m++;
        }
        while(m<5);


    }
}
